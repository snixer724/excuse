function random(maxNum) {
  return Math.floor(Math.random() * (maxNum));
}

function randomString(strings) {
  var randomNumber = random(strings.length);
  return strings[randomNumber]
}

function newExcuse() {
  var apology = randomString(excuseGen["apology"])
  document.getElementById('apologyDisplay').innerHTML = apology;

  var subject = randomString(excuseGen["subject"])
  document.getElementById('subjectDisplay').innerHTML = subject;

  var reason = randomString(excuseGen["reason"])
  document.getElementById('reasonDisplay').innerHTML = reason;
}
